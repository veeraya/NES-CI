        <h3>Project Background</h3>
        <p>Until recently, Sri Lanka had for a long time been irreversibly etched in global perspective as a war-torn country without a way out of conflict and turmoil. Batticaloa is largely agrarian region in the Eastern Province of  Sri Lanka with a population of about half a million people, Batticaloa was heavily affected by war like other regions in the North and East of Sri Lanka, largely due to the LTTE's reliance on this region for a significant proportion of its recruitment. </p>

        <p>Batticaloa was also one of the worst hit coastal areas in the 2004 Tsunami. Nearly 10,000 homes were destroyed and almost 3,000 people lost their lives, and the rebuilding process was disrupted when hostilities between the government and the LTTE broke out in the Eastern Province in 2006. In many other post-conflict zones in countries such as Liberia and Rwanda, entrepreneurship is seen as one of the key drivers in increasing trade, growth and employment in the region. Similarly, we recognized the importance of Entrepreneurship in promoting economic activity in Batticaloa. Thus, we sought to expedite the redevelopment of the region by inculcating an entrepreneurial culture there.</p>

        <h3>Maalu Maalu Lanka: Empowering Young Entrepreneurs</h3>
        <p>The project was kick started by a group of passionate undergraduates from the National University of Singapore with a love for the region of Batticaloa and a deep interest in helping to uplift the community there. We gathered like-minded professors and entrepreneurs from NUS Enterprise who are well versed in nurturing entrepreneurship and exposed them to the situation in Batticaloa. </p>

        <p>To address the fledgling state of entrepreneurship there, Maalu Maalu Lanka is embarking on collaborating with a group of expert professors and entrepreneurs well versed in nurturing entrepreneurship from the National University of Singapore's Enterprise Centre to kick start the creation of an entrepreneurial community in Batticaloa.  </p>

        <p>To achieve this, we will partner with Bureaucrats there who are keen to fuel the redevelopment of the region and inaugurate an Entrepreneurship Society for young aspiring entrepreneurs in Batticaloa. The Society will be made up of undergraduates who have yet to delve into entrepreneurship and individuals that have already undertaken successful business activities. </p>

